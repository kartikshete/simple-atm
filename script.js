// ===== Initialize from localStorage =====
function loadData() {
    const savedData = localStorage.getItem('atm_data');
    if (savedData) {
        const data = JSON.parse(savedData);
        ATM.pin = data.pin || null;
        ATM.balance = data.balance ?? 50000;
        ATM.language = data.language || 'en';
        ATM.profile = data.profile || { name: '', photo: '' };
        ATM.transactions = (data.transactions || []).map(tx => ({
            ...tx,
            date: new Date(tx.date)
        }));
    }
}

function saveData() {
    localStorage.setItem('atm_data', JSON.stringify({
        pin: ATM.pin,
        balance: ATM.balance,
        transactions: ATM.transactions,
        language: ATM.language,
        profile: ATM.profile
    }));
}

