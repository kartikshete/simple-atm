// ===== Withdraw Logic =====
function initiateWithdraw() {
    const amount = parseFloat(elements.withdrawAmount.value);

    if (isNaN(amount) || amount <= 0) {
        elements.withdrawError.textContent = t('invalidAmount');
        return;
    }
    if (amount < 100) {
        elements.withdrawError.textContent = t('minWithdraw');
        return;
    }
    if (amount % 100 !== 0) {
        elements.withdrawError.textContent = t('multipleOf100');
        return;
    }
    if (amount > ATM.balance) {
        elements.withdrawError.textContent = t('insufficientBalance');
        return;
    }
    if (amount > 50000) {
        elements.withdrawError.textContent = t('maxWithdraw');
        return;
    }

    // Amount valid -> Request PIN
    ATM.transactionAmount = amount;
    ATM.transactionType = 'withdraw';
    elements.verifyPinPanel.classList.add('active');
}

function performWithdraw(amount) {
    ATM.balance -= amount;
    ATM.transactions.unshift({
        type: 'withdraw',
        amount: amount,
        date: new Date(),
        balance: ATM.balance
    });

    saveData();
    showSuccess(t('transactionSuccess'), `₹${amount.toLocaleString('en-IN')} ${t('withdrawSuccess')}`, amount, 'withdraw');
}

